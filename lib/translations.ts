// Función para traducir texto
export function translateText(text: string, category: keyof typeof translations = 'generic'): string {
  if (!text) return text
  
  // Buscar traducción exacta
  const categoryTranslations = translations[category] as Record<string, string>
  if (categoryTranslations && categoryTranslations[text]) {
    return categoryTranslations[text]
  }
  
  // Buscar en todas las categorías
  for (const categoryKey in translations) {
    const categoryTranslations = translations[categoryKey as keyof typeof translations] as Record<string, string>
    if (categoryTranslations && categoryTranslations[text]) {
      return categoryTranslations[text]
    }
  }
  
  // Si no se encuentra traducción, devolver el texto original
  return text
}

// Función para traducir múltiples términos separados por comas
export function translateMultipleTerms(text: string, separator: string = ', '): string {
  if (!text) return text
  
  const terms = text.split(separator)
  const translatedTerms = terms.map(term => translateText(term.trim()))
  return translatedTerms.join(separator)
}

