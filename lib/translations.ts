// Función para buscar la clave en inglés basada en el valor en español
export function findEnglishKey(spanishValue: string, category?: keyof typeof translations): string | null {
  if (category) {
    const categoryTranslations = translations[category]
    for (const [key, value] of Object.entries(categoryTranslations)) {
      if (value === spanishValue) {
        return key
      }
    }
  } else {
    // Buscar en todas las categorías
    for (const categoryKey in translations) {
      const categoryTranslations = translations[categoryKey as keyof typeof translations]
      for (const [key, value] of Object.entries(categoryTranslations)) {
        if (value === spanishValue) {
          return key
        }
      }
    }
  }
  return null
}